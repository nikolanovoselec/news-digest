#!/usr/bin/env node
/**
 * Generate TypeScript fixtures that embed raw source-file contents
 * as string constants. Used by tests that need to assert on the
 * literal text of `src/styles/global.css` (the `?raw` / `?raw-css`
 * import suffixes don't survive the @cloudflare/vitest-pool-workers
 * bundle for .css files).
 *
 * Run via `npm run fixtures` or automatically as a `pretest` /
 * `pretypecheck` step. The output file is committed so IDE + tsc
 * work without a mandatory pre-run.
 */
import { readFileSync, writeFileSync, mkdirSync } from 'node:fs';
import { dirname, join } from 'node:path';
import { fileURLToPath } from 'node:url';

const scriptDir = dirname(fileURLToPath(import.meta.url));
const repoRoot = join(scriptDir, '..');

const sources = [
  {
    from: 'src/styles/global.css',
    to: 'tests/fixtures/global-css.ts',
    exportName: 'GLOBAL_CSS'
  }
];

for (const { from, to, exportName } of sources) {
  const srcPath = join(repoRoot, from);
  const outPath = join(repoRoot, to);
  const content = readFileSync(srcPath, 'utf-8');
  const body =
    `// Auto-generated by scripts/generate-test-fixtures.mjs from ${from}.\n` +
    `// Do not edit directly; regenerate via \`npm run fixtures\`.\n` +
    `/* eslint-disable */\n` +
    `export const ${exportName} = ${JSON.stringify(content)};\n`;
  mkdirSync(dirname(outPath), { recursive: true });
  writeFileSync(outPath, body, 'utf-8');
  console.log(`[fixtures] wrote ${to} (${content.length} chars)`);
}
