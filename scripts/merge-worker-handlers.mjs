// Post-build shim: merges scheduled + queue handlers from src/worker.ts
// into the Astro-built Cloudflare Worker entry at dist/_worker.js.
//
// Why this exists: the @astrojs/cloudflare adapter's `workerEntryPoint`
// option produced an invalid merged worker (Cloudflare validator error
// 10021: "(void 0) is not a function"). Running the merge ourselves keeps
// Astro's generated fetch handler untouched and composes our named
// handlers on top.
//
// Output layout inside dist/_worker.js/:
//   index.js            — Astro's generated entry (default: { fetch })
//   handlers-bundle.mjs — our scheduled/queue, bundled via esbuild
//   _merged.mjs         — the wrapper wrangler deploys (new `main`)
//
// wrangler.toml points `main` at `_merged.mjs`. Assets upload skips the
// whole dist/_worker.js folder via dist/.assetsignore.

import * as esbuild from 'esbuild';
import { writeFile } from 'node:fs/promises';
import { existsSync } from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const root = path.resolve(__dirname, '..');
const distWorkerDir = path.join(root, 'dist/_worker.js');
const astroEntry = path.join(distWorkerDir, 'index.js');

if (!existsSync(astroEntry)) {
  console.error(
    `[merge-worker-handlers] ${astroEntry} not found — did 'astro build' run first?`,
  );
  process.exit(1);
}

// Bundle src/worker.ts to a single ESM file alongside Astro's entry.
// Shares the `~/*` alias convention from tsconfig.json so imports like
// `~/lib/discovery` resolve to src/lib/discovery.
await esbuild.build({
  entryPoints: [path.join(root, 'src/worker.ts')],
  bundle: true,
  format: 'esm',
  platform: 'neutral',
  target: 'es2022',
  outfile: path.join(distWorkerDir, 'handlers-bundle.mjs'),
  alias: {
    '~': path.join(root, 'src'),
  },
  // Runtime-provided built-ins stay external.
  external: ['cloudflare:*'],
  logLevel: 'warning',
});

const merged = `// Auto-generated by scripts/merge-worker-handlers.mjs.
// Merges Astro's fetch-only default export with scheduled + queue handlers.
import astroEntry from './index.js';
import { scheduled, queue } from './handlers-bundle.mjs';
export default { ...astroEntry, scheduled, queue };
`;

await writeFile(path.join(distWorkerDir, '_merged.mjs'), merged, 'utf8');

console.log(
  '[merge-worker-handlers] wrote dist/_worker.js/_merged.mjs and handlers-bundle.mjs',
);
